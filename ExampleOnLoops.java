import java.util.Scanner;
public class ExampleOnLoops {
    // A java program to run a loop
    //remember all java programs start with a call to main
    public static void main(String args[]){
        System.out.println("My loop will produce the following output..!!");
        for (int MyVal = 15; MyVal >= 0; MyVal--){
            System.out.println(MyVal);
        }
    }
}






